            function timer() {
                return {
                    targetDate: new Date('2024-09-06T14:00:00'),
                    days: '00',
                    hours: '00',
                    minutes: '00',
                    seconds: '00',
                    init() {
                        this.updateTimer();
                        setInterval(() => this.updateTimer(), 1000);
                    },
                    updateTimer() {
                        const now = new Date();
                        const timeDiff = this.targetDate - now;

                        if (timeDiff <= 0) {
                            this.days = '00';
                            this.hours = '00';
                            this.minutes = '00';
                            this.seconds = '00';
                            return;
                        }

                        this.days = Math.floor(timeDiff / (1000 * 60 * 60 * 24)).toString().padStart(2, '0');
                        this.hours = Math.floor((timeDiff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60)).toString().padStart(2, '0');
                        this.minutes = Math.floor((timeDiff % (1000 * 60 * 60)) / (1000 * 60)).toString().padStart(2, '0');
                        this.seconds = Math.floor((timeDiff % (1000 * 60)) / 1000).toString().padStart(2, '0');
                    }
                }
